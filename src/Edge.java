public class Edge {

    private int source;
    private int destiny;

    public Edge(int source, int destiny) {
        this.source = source;
        this.destiny = destiny;
    }

    public int getSource() {
        return source;
    }

    public int getDestiny() {
        return destiny;
    }
}
